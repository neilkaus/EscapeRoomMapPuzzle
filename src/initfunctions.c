//Intialize timer 2 to fire interupts with a period of period ms 
// this does not start the timer TIM2_Start() must be called
void TIM2_Init(uint16_t period){
  __TIM2_CLK_ENABLE();
  TIM2->PSC = 7199;               // Prescaler value, 7200 - 1, 72 MhZ / 7200 = 10000 Hz
  TIM2->EGR = TIM_EGR_UG;         // Re-initialize the counter and generates an update of the registers (to have prescaler take effect)
  TIM2->ARR = 10*period-1;        // set ARR so the period of firing is period ms
  TIM2->DIER = TIM_DIER_UIE;      // Enable update based interrupts for this timer
  NVIC_EnableIRQ(TIM2_IRQn);      // Enable interrupts for this timer in the interupt controller
}

